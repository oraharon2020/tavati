### עלות תיקון
קבלו 2-3 הצעות מחיר מקבלנים אחרים לתיקון הבעיות.

### החזר על עבודה לא גמורה
שילמת על 100%, קיבלת 70%? מגיע לך 30% בחזרה.

### נזקים נגררים
- שכירות נוספת (אם נאלצת להישאר מחוץ לבית)
- נזק לרכוש
- הוצאות בלתי צפויות

### עוגמת נפש
2,000-5,000 ₪ על הסבל וההתעסקות.

## תירוצים של קבלנים

**"שינית את הדרישות"**
אם היו שינויים - הם צריכים להיות מתועדים ומוסכמים.

**"החומרים התייקרו"**
הצעה סגורה = הבעיה שלו.

**"זה בגלל הפועלים"**
הוא אחראי על הצוות שלו.

**"אתה לא מבין בעבודה"**
לא צריך להיות מומחה כדי לראות שמשהו עקום.

## לפני שמגישים תביעה

1. **שלחו מכתב דרישה** - תנו לו הזדמנות לתקן
2. **תעדו הכל** - תמונות, הצעות מחיר, התכתבויות
3. **חשבו על פשרה** - לפעמים 70% עכשיו עדיף על 100% בעוד שנה

